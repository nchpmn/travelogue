					<p>
						The only consolidated city-county in <a href="#" title="">California</a>, San Francisco encompasses a land area of about 46.9 square miles (121 km2) on the northern end of the San Francisco Peninsula, giving it a density of about 17,620 people per square mile (6,803 people per km2). Francisco is the fourth most populous city in California, after Los Angeles, San Diego and San Jose, and the 14th most populous city in the United States—with a Census-estimated 2012 population of 825,863. The city is also the financial and cultural hub of the larger, <mark>San Jose-San Francisco-Oakland metropolitan area</mark>, with a population of 8.4 million.
